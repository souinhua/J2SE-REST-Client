    public String post(String url) {
        this.setTitle("HTTP POST Request");
        this.urlTextField.setText(url);
        this.actionBtn.setText("POST");
        this.dialogType = PostPutDialog.HTTP_POST;

        this.centerize();
        this.clearBtnActionPerformed(null);
        this.setVisible(true);
        return this.returnValue;
    }

