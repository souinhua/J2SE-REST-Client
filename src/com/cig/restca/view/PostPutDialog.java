    public String getURL() {
        return this.urlTextField.getText();
    }

    private synchronized void formatData() {
        if (formatComboBox.getSelectedItem().toString().equals("JSON")) {
            JSONObject json = new JSONObject();

            int rowCount = dataTable.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                String field = (String) dataTable.getValueAt(i, 0);
                String value = (String) dataTable.getValueAt(i, 1);
                if (field!=null) {
                    json.put(field, value);
                }
            }

            formatTextArea.setText(json.toString(5));
        }
    }
    
    @Override
    public void centerize() {
        int parentWidth = this.getParent().getWidth();
        int parentHeight = this.getParent().getHeight();

        int width = this.getWidth();
        int height = this.getHeight();

        int pX = this.getParent().getX();
        int pY = this.getParent().getY();

        this.setLocation(pX + ((parentWidth / 2) - (width / 2)), pY + ((parentHeight / 2) - (height / 2)));
    }

